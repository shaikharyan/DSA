// In C++, type casting refers to the process of converting the data type of an object or variable into another data type. It allows you to explicitly change the interpretation or representation of a value from one type to another. Type casting can be useful when you need to perform operations that involve different data types or when you want to assign a value of one type to a variable of another type.



#include <iostream>
using namespace std;
int main()
{
    int a = 'a';
    cout << a << endl;

    char ch = 98;
    cout << ch << endl;

    return 0;
}
